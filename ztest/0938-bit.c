// Test function for signed char
int test_bitop_schar(test_case_schar *cases, int n, const char *type_name) {
    for (int i = 0; i < n; i++) {
        signed char and_result = cases[i].val & cases[i].mask;
        signed char or_result = cases[i].val | cases[i].mask;
        signed char xor_result = cases[i].val ^ cases[i].mask;
        if (and_result != cases[i].expect_and) {
            printf("FAIL: %s case %d: 0x%02x & 0x%02x = 0x%02x (expected 0x%02x)\n",
                   type_name, i+1, cases[i].val, cases[i].mask, and_result, cases[i].expect_and);
            return i+1;
        }
        if (or_result != cases[i].expect_or) {
            printf("FAIL: %s case %d: 0x%02x | 0x%02x = 0x%02x (expected 0x%02x)\n",
                   type_name, i+1, cases[i].val, cases[i].mask, or_result, cases[i].expect_or);
            return i+1;
        }
        if (xor_result != cases[i].expect_xor) {
            printf("FAIL: %s case %d: 0x%02x ^ 0x%02x = 0x%02x (expected 0x%02x)\n",
                   type_name, i+1, cases[i].val, cases[i].mask, xor_result, cases[i].expect_xor);
            return i+1;
        }
    }
    printf("%s: All bitwise operator tests passed!\n", type_name);
    return 0;
}

